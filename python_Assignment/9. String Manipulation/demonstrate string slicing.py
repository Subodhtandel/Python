string = "Hello World!"

print("Original String:",string)

print("Characters from 0 to 4:",string[0:5])     
print("Characters from 5 to end:",string[5:])    
print("First 3 characters:",string[:3])                
print("Last 3 characters:",string[-3:])                
print("Every 2nd character:",string[::2])              
print("Reversed string:",string[::-1])                 
