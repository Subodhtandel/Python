string = "  hello world! Welcome to Python Programming.  "

print("Original String:",string)

print("After strip():",string.strip())

print("Uppercase:",string.upper())

print("Lowercase:",string.lower())

print("Capitalize:",string.capitalize())

print("Title:",string.title())

print("Replace 'Python' with 'Java':",string.replace("Python", "Java"))

print("Count of 'o':",string.count("o"))

print("Position of 'world':",string.find("world"))

print("Starts with 'hello':",string.strip().startswith("hello"))

print("Ends with '.':",string.strip().endswith("."))
