import math

num = 25
decimal_num = 15.75

print("Number:", num)
print("Decimal Number:", decimal_num)

print("Square root of", num, ":", math.sqrt(num))

print("2 raised to 3:", math.pow(2, 3))

print("Factorial of 5:", math.factorial(5))

print("Ceil of", decimal_num, ":", math.ceil(decimal_num))

print("Floor of", decimal_num, ":", math.floor(decimal_num))

print("Absolute value of -10:", math.fabs(-10))

print("Value of pi:", math.pi)

print("Value of e:", math.e)
